        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult RoleAddToUser(string UserName,string RoleName)
        {
            try
            {


                var user = _db.Users.Where(u => u.UserName.Equals(UserName, StringComparison.CurrentCultureIgnoreCase)).FirstOrDefault();
                var manager = new ApplicationUserManager(new UserStore<Uzytkownik>(_db));
                manager.AddToRole(user.Id, RoleName);
                ViewBag.Message = "Sukces";
            }
            catch {
                ViewBag.Message = "Coś poszło nie tak";
            }
            var list = _db.Roles.OrderBy(r => r.Name).ToList().Select(rr => new SelectListItem { Value = rr.Name.ToString(), Text = rr.Name }).ToList();
            ViewBag.Roles = list;

            return View("ManagUsers");
        }

        [HttpPost]
        [ValidateAntiForgeryToken]
        public ActionResult GetRoles(string UserName)
        {
            try
            {
                if (!string.IsNullOrWhiteSpace(UserName))
                {
                    var user = _db.Users.Where(u => u.UserName.Equals(UserName, StringComparison.CurrentCultureIgnoreCase)).FirstOrDefault();
                    var manager = new ApplicationUserManager(new UserStore<Uzytkownik>(_db));
                    ViewBag.RolesForThisUser = manager.GetRoles(user.Id);
                }
                ViewBag.Message = "Sukces";
            }
            catch
            {
                ViewBag.Message = "Coś poszło nie tak";
            }
            var list = _db.Roles.OrderBy(r => r.Name).ToList().Select(rr => new SelectListItem { Value = rr.Name.ToString(), Text = rr.Name }).ToList();
            ViewBag.Roles = list;

            return View("ManagUsers");
        }

